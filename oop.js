/*
    ENCAPSULATION
    Grouping together variables (properties) and functions (methods) that are related into objects.
    Reduces complexity, increases reusability.

    ABSTRACTION
    Only make visible the properties/methods that are going to be used from outside.
    Changing private properties/methods is less risky. It prevents them from being manipulated from outside.

    INHERITANCE
    Allows to "inherit" properties/methods from a parent class. Eliminates redundant code.

    POLYMORPHISM
    Allows to override the implementation of an inherited method.
*/
